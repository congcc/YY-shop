    {
            $req = $request->only('password','id');
            
            //对密码进行哈希加密
            $req['password'] = Hash::make($request->input('password'));

            $res = pay::where('uid', $req['id'])->update(['password'=>$req['password']]);

            if ($res) {
                echo 1;
            }
     
